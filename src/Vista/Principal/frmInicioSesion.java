    private void btnIngresarProfActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarProfActionPerformed
        ProfesorDAO mode = new ProfesorDAO();

        String usuario = txtUsuarioProf.getText();
        String passw = new String(txtContrasenniaProf.getPassword());

        if (usuario.equals("") || passw.equals("")) {
            JOptionPane.showMessageDialog(null, "Error al ingresar los datos");
        } else {
            mode.acceder(usuario, passw);
            dispose();
        }

    }//GEN-LAST:event_btnIngresarProfActionPerformed

