    private void btnIngresarAdminActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarAdminActionPerformed

        String usu = txtUsuarioAdmin.getText();
        String pass = new String(txtContrasenniaAdmin.getPassword());

        if (usu.equals("") || pass.equals("")) {
            JOptionPane.showMessageDialog(null, "Error al ingresar los datos");
        } else if (usu.equals("admin") && pass.equals("admin")) {
            frmVentanaPrincipal ventana = new frmVentanaPrincipal();
            ventana.setVisible(true);
            ventana.menuProfesor.setVisible(false);
            ventana.menuEstudiante.setVisible(false);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Administrador NO se encuentra Registrado en el sistema\nIngrese de nuevo!", "AVISO", JOptionPane.WARNING_MESSAGE);
            txtUsuarioAdmin.setText("");
            txtContrasenniaAdmin.setText("");
        }

    }//GEN-LAST:event_btnIngresarAdminActionPerformed

