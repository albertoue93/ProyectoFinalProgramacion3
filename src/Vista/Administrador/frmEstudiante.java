    public void crearEstudiante(String cedulaEst, String nombreEst, String apeEst, String emailEst, String teleEst, String direcEst, String sexoEst, String tipoEst, String usuario, String contra) {
        EstudianteVO est = new EstudianteVO();
        modelo = new EstudianteDAO();

        est.setCedulaEstudiante(cedulaEst);
        est.setNombreEstudiante(nombreEst);
        est.setApellidosEstudiante(apeEst);
        est.setEmailEstudiante(emailEst);
        est.setTelefonoEstudiante(teleEst);
        est.setDireccionEstudiante(direcEst);
        est.setSexoEstudiante(sexoEst);
        est.setTipoUsuario(tipoEst);
        est.setUsuario(usuario);
        est.setContrasennia(contra);

        modelo.crearEstudiante(est);
    }

    public void modificarEstudiante(int idEstudiante, String cedulaEst, String nombreEst, String apeEst, String emailEst, String teleEst, String direcEst, String sexoEst, String tipoEst, String usuario, String contra) {
        EstudianteVO est = new EstudianteVO();
        modelo = new EstudianteDAO();

        est.setIdEstudiante(idEstudiante);

        est.setCedulaEstudiante(cedulaEst);
        est.setNombreEstudiante(nombreEst);
        est.setApellidosEstudiante(apeEst);
        est.setEmailEstudiante(emailEst);
        est.setTelefonoEstudiante(teleEst);
        est.setDireccionEstudiante(direcEst);
        est.setSexoEstudiante(sexoEst);
        est.setTipoUsuario(tipoEst);
        est.setUsuario(usuario);
        est.setContrasennia(contra);

        modelo.modificarEstudiante(est);
    }

    public void eliminarEstudiante(int idEstudiante) {
        EstudianteVO est = new EstudianteVO();
        modelo = new EstudianteDAO();

        est.setIdEstudiante(idEstudiante);
        modelo.eliminarEstudiante(est);
    }

    void mostrarEstudiantes(String buscar) {
        try {
            DefaultTableModel tabla;
            EstudianteDAO est = new EstudianteDAO();
            tabla = est.mostrarEstudiantes(buscar);
            tablaAlumno.setModel(tabla);
        } catch (Exception e) {
        }
    }

