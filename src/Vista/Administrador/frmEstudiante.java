    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        String cedulaEst = txtCedulaAlumno.getText();
        String nombreEst = txtNombreAlumno.getText();
        String apeEst = txtApellidosAlumno.getText();
        String emailEst = txtEmailAlumno.getText();
        String teleEst = txtTelefonoAlumno.getText();
        String direcEst = txtDireccionAlumno.getText();
        String sexoEst = comboboxSexoAlumno.getSelectedItem().toString();
        String tipoEst = ComboBoxTipoUsuario.getSelectedItem().toString();
        String usuario = txtUsuario.getText();
        String contra = txtContrasennia.getText();

        if (txtCedulaAlumno.getText().equals("")
                || txtNombreAlumno.getText().equals("")
                || txtApellidosAlumno.getText().equals("")
                || txtEmailAlumno.getText().equals("")
                || txtTelefonoAlumno.getText().equals("")
                || txtDireccionAlumno.getText().equals("")
                || txtUsuario.getText().equals("")
                || txtContrasennia.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Debe ingresar todos los datos necesarios");
        } else {
            try {
                this.crearEstudiante(cedulaEst, nombreEst, apeEst, emailEst, teleEst, direcEst, sexoEst, tipoEst, usuario, contra);
                JOptionPane.showMessageDialog(null, "Estudiante registrado con Exito!");
                limpiar();
                mostrarEstudiantes("");
            } catch (Exception e) {
            }
        }

    }//GEN-LAST:event_btnAgregarActionPerformed

