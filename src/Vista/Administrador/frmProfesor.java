    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        String cedulaProf = txtCedula.getText();
        String nombreProf = txtNombreProfesor.getText();
        String apeProf = txtApellidosProfesor.getText();
        String emailProf = txtEmailProfesor.getText();
        String teleProf = txtTelefonoProfesor.getText();
        String direcProf = txtDireccionProfesor.getText();
        String sexoProf = comboboxSexoProfesor.getSelectedItem().toString();
        String tipoProf = ComboBoxTipoUsuario.getSelectedItem().toString();
        String usuario = txtUsuario.getText();
        String contra = txtContrasennia.getText();

        if (txtCedula.getText().equals("")
                || txtNombreProfesor.getText().equals("")
                || txtApellidosProfesor.getText().equals("")
                || txtEmailProfesor.getText().equals("")
                || txtTelefonoProfesor.getText().equals("")
                || txtDireccionProfesor.getText().equals("")
                || txtUsuario.getText().equals("")
                || txtContrasennia.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Debe ingresar todos los datos necesarios");
        } else {
            try {
                this.crearEstudiante(cedulaProf, nombreProf, apeProf, emailProf, teleProf, direcProf, sexoProf, tipoProf, usuario, contra);
                JOptionPane.showMessageDialog(null, "Profesor registrado con Exito!");
                limpiar();
                mostrarProfesor("");
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

