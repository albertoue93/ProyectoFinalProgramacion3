    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int idProfesor = Integer.parseInt(txtIDProfesor.getText());
        int op = JOptionPane.showConfirmDialog(null, "¿Desea eliminar este Profesor/Administrativo?");

        if (op == 0) {
            try {
                this.eliminarEstudiante(idProfesor);
                JOptionPane.showMessageDialog(null, "Profesor eliminar con Exito!");
                limpiar();
            } catch (Exception e) {
            }
        }
        mostrarProfesor("");
    }//GEN-LAST:event_btnEliminarActionPerformed

