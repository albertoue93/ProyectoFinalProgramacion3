    public void crearEstudiante(String cedulaProf, String nombreProf, String apeProf, String emailProf, String teleProf, String direcProf, String sexoProf, String tipoProf, String usuario, String contra) {
        ProfesorVO prof = new ProfesorVO();
        modelo = new ProfesorDAO();

        prof.setCedulaProfesor(cedulaProf);
        prof.setNombreProfesor(nombreProf);
        prof.setApellidosProfesor(apeProf);
        prof.setEmailProfesor(emailProf);
        prof.setTelefonoProfesor(teleProf);
        prof.setDireccionProfesor(direcProf);
        prof.setSexoProfesor(sexoProf);
        prof.setTipoUsuario(tipoProf);
        prof.setUsuario(usuario);
        prof.setContrasennia(contra);

        modelo.crearProfesor(prof);
    }

    public void modificarEstudiante(int idProf, String cedulaProf, String nombreProf, String apeProf, String emailProf, String teleProf, String direcProf, String sexoProf, String tipoProf, String usuario, String contra) {
        ProfesorVO prof = new ProfesorVO();
        modelo = new ProfesorDAO();

        prof.setIdProfesor(idProf);

        prof.setCedulaProfesor(cedulaProf);
        prof.setNombreProfesor(nombreProf);
        prof.setApellidosProfesor(apeProf);
        prof.setEmailProfesor(emailProf);
        prof.setTelefonoProfesor(teleProf);
        prof.setDireccionProfesor(direcProf);
        prof.setSexoProfesor(sexoProf);
        prof.setTipoUsuario(usuario);
        prof.setUsuario(usuario);
        prof.setContrasennia(contra);

        modelo.modificarProfesor(prof);
    }

    public void eliminarEstudiante(int idProfesor) {
        ProfesorVO prof = new ProfesorVO();
        modelo = new ProfesorDAO();

        prof.setIdProfesor(idProfesor);

        modelo.eliminarProfesor(prof);
    }

    void mostrarProfesor(String buscar) {
        try {
            DefaultTableModel tabla;
            ProfesorDAO prof = new ProfesorDAO();
            tabla = prof.mostrarProfesor(buscar);
            tablaProfesor.setModel(tabla);
        } catch (Exception e) {
        }
    }

