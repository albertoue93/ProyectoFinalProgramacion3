    public void crearAsistencia(int idEst, int idProfe, int idCurso, String fechaF, String justificacion) {
        AsistenciaVO asistencia = new AsistenciaVO();
        modelo = new AsistenciaDAO();

        asistencia.setIdEstudiante(idEst);
        asistencia.setIdProfesor(idProfe);
        asistencia.setIdCurso(idCurso);
        asistencia.setFechaFalta(fechaF);
        asistencia.setJustificacion(justificacion);

        modelo.crearAsistencia(asistencia);
    }

    public void modificarAsistencia(int idAsistencia, int idEst, int idProfe, int idCurso, String fechaF, String justificacion) {
        AsistenciaVO asistencia = new AsistenciaVO();
        modelo = new AsistenciaDAO();

        asistencia.setIdAsistencia(idAsistencia);
        asistencia.setIdEstudiante(idEst);
        asistencia.setIdProfesor(idProfe);
        asistencia.setIdCurso(idCurso);
        asistencia.setFechaFalta(fechaF);
        asistencia.setJustificacion(justificacion);

        modelo.modificarAsistencia(asistencia);
    }

