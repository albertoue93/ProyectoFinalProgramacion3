    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        int idEst = Integer.parseInt(txtIdEstudiante.getText());
        int idProfe = Integer.parseInt(txtIdProfesor.getText());
        int idCurso = Integer.parseInt(txtIdCurso.getText());
        String fechaF = txtFecha.getText();
        String justificacion = cmbJustificacion.getSelectedItem().toString();

        try {
            this.crearAsistencia(idEst, idProfe, idCurso, fechaF, justificacion);
            mostrarAsistencia(txtBuscar.getText());
            JOptionPane.showMessageDialog(null, "Asistencia ingresada con Exito!");
            limpiar();
            btnIngresar.setEnabled(false);
            btnModificar.setEnabled(false);
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnIngresarActionPerformed

