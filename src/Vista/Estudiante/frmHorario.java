    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        if (txtBuscar.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Debe ingresar su ID de Estudiante");
        } else {
            mostrarCurso(txtBuscar.getText());
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void tablaCursoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaCursoMouseClicked
        int fila = tablaCurso.rowAtPoint(evt.getPoint());

        txtCurso.setText(tablaCurso.getValueAt(fila, 0).toString());
    }//GEN-LAST:event_tablaCursoMouseClicked

    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarActionPerformed
        if (txtCurso.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un curso!");
        } else {
            mostrarHorario(txtCurso.getText());
        }
    }//GEN-LAST:event_btnMostrarActionPerformed

    void mostrarCurso(String buscar) {
        try {
            DefaultTableModel modelo;
            ListasDAO horario = new ListasDAO();
            modelo = horario.mostrarCursos(buscar);
            tablaCurso.setModel(modelo);
        } catch (Exception e) {
        }
    }

    void mostrarHorario(String buscar) {
        try {
            DefaultTableModel modelo;
            ListasDAO horario = new ListasDAO();
            modelo = horario.mostrarHorario(buscar);
            tablaHorario.setModel(modelo);
        } catch (Exception e) {
        }
    }

